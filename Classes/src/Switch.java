public class Switch {

    public static void main(String[] args) {
        int num =8;
        switch (num){
            case 1*8:
                System.out.println("I am one!");
                break;
            case 4:
                System.out.println("I am case 4");
            case 2-5:
                System.out.println("I am one!");
            default:
                System.out.println("I am default");
        }
    }


}
